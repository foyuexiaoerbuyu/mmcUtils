package org.mmc.util.socket;


/**
 * 该类定义了聊天消息的类型常量。
 */
public class ChatMsgType {
    /**
     * MSG_TYPE_MSG: 表示文本消息类型
     */
    public static final int MSG_TYPE_MSG = 0;
    /**
     * MSG_TYPE_FILE: 表示文件消息类型
     */
    public static final int MSG_TYPE_FILE = 1;
    /**
     * MSG_TYPE_LOG: 表示日志消息类型
     */
    public static final int MSG_TYPE_LOG = 2;
    /**
     * MSG_TYPE_CMD: 表示命令消息类型
     */
    public static final int MSG_TYPE_CMD = 3;
}